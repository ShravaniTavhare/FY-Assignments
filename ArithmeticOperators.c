//To understand Arithmetic Operations in C

#include <stdio.h>

int main() {
    int a=20, b=5;
    
    printf ("a+b= %d\n", a+b);         //PLUS

    printf ("a-b= %d\n", a-b);         //MINUS

    printf ("a*b= %d\n", a*b);         //MULTIPLY

    printf ("a/b= %d\n", a/b);         //DIVIDE

    printf ("a%b= %d\n", a%b);        //MODULUS

    return 0;
}